package com.example.asus.activiteas.logic;

public class Options {
    private double level;
    private int people;

    public Options(){
    }

    public void setLevel(double level){
        this.level=level;
    }

    public double getLevel(){
        return level;
    }

    public void setPeople(int people){
        this.people = people;
    }

    public int getPeople(){
        return people;
    }

}
